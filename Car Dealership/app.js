const sectionCenter = document.querySelector(".section-center");

window.addEventListener("DOMContentLoaded", function () {
  displayCars(cars);
});

function displayCars(Cars) {
  let showCars = Cars.map(function (car) {
    return `<article class="single-car">
        <img src=${car.img} alt="" class="img"/>
        <div class="car-info">
          <h4 class="car-name">${car.model}</h4>
          <p class="time">   </p>
          <h5 class="car-price">price: ${car.price}$</h5>
          <p class="car-desc">
            ${car.desc}
          </p>
        </div>
      </article>`;
  });
  showCars = showCars.join("");
  sectionCenter.innerHTML = showCars;
}

const filterBtns = document.querySelectorAll(".filter-btn");

filterBtns.forEach(function (btn) {
  btn.addEventListener("click", function (e) {
    const manufacturer = e.currentTarget.dataset.id;
    const carManufacturer = cars.filter(function (car) {
      if (car.manufacturer === manufacturer) {
        return car;
      }
    });
    if (manufacturer === "all") {
      return displayCars(cars);
    } else {
      displayCars(carManufacturer);
    }
  });
});

//set the date we're counting down to

let countdownDate = new Date("Sep 1, 2022 00:00:00").getTime();

//this updates time every one second
let timeFunction = setInterval(function () {
  //get today's date
  let now = new Date().getTime();

  //calculate time till the countdown
  let remainingTime = countdownDate - now;

  //calculationd for days, hours, minutes, seconds
  let days = Math.floor(remainingTime / (1000 * 60 * 60 * 24));
  let hours = Math.floor(
    (remainingTime % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60)
  );
  let minutes = Math.floor((remainingTime % (1000 * 60 * 60)) / (1000 * 60));
  let seconds = Math.floor((remainingTime % (1000 * 60)) / 1000);

  //display result on html page
  document.querySelector(".time").innerHTML =
    days + " D " + hours + " H " + minutes + " M " + seconds + " s ";

  if (remainingTime < 0) {
    clearInterval(timeFunction);
    document.querySelector(".time").innerHTML = "Expired";
  }
}, 1000);
